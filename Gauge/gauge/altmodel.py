# --------------------------------------------------------------------------- #
# Cohort generation (dispersion ground truth) + the two D*eff surrogates.
# --------------------------------------------------------------------------- #
def _dispersion_pool(seed, cv, b):
    """One seeded dispersion cohort (D, mu, f) + per-voxel SNR + noisy signals.

    ``params`` columns are (D, mu, f): the gamma-mean ``mu`` IS surrogate-A D*eff.
    At ``cv=0`` the generator is the exact bi-exponential limit (continuity).
    """
    rng = np.random.default_rng(seed + 404)
    n = N_TRAIN + N_CAL + N_TEST
    params = _draw_params(n, rng)                       # (D, D*->mu, f)
    snr = _draw_snr(n, rng, DEFAULT_SNR_GRID)
    sig = _simulate(b, params, snr, rng, model="dispersion", disp_cv=cv)
    return params, snr, sig


def _lognormal_pool(seed, cv, b):
    """A log-normal-kernel dispersion cohort, mirroring :func:`_dispersion_pool`.

    Uses the SAME seeded (D, mu, f) / SNR / noise draws as the gamma pool (rng
    offset ``seed + 404``) and swaps ONLY the dispersion-kernel shape (gamma ->
    log-normal). So the gamma and log-normal Arm-1 cohorts differ in nothing but the
    kernel shape -- an apples-to-apples second confirmation. ``mu`` (column 1) is
    surrogate-A D*eff exactly as in the gamma model.
    """
    rng = np.random.default_rng(seed + 404)
    n = N_TRAIN + N_CAL + N_TEST
    params = _draw_params(n, rng)                       # (D, mu, f)
    snr = _draw_snr(n, rng, DEFAULT_SNR_GRID)
    clean = ivim_signal_lognormal_dispersion(b[None, :], params[:, 0:1],
                                             params[:, 1:2], cv, params[:, 2:3],
                                             S0=1.0)
    sig = add_rician_noise(clean, snr[:, None], rng, S0=1.0)
    return params, snr, sig


def _bestfit_biexp_dstar(params, cv, b):
    """Surrogate B: best-fit bi-exponential D* to the CLEAN dispersion signal.

    Deterministic (no noise) -> a ground-truth-derived stratification axis, not a
    noisy estimate. ``cv=0`` returns mu exactly (clean signal is bi-exponential).
    """
    D, mu, f = params[:, 0], params[:, 1], params[:, 2]
    k = np.inf if cv <= 0 else 1.0 / (cv * cv)
    clean = ivim_signal_dispersion(b[None, :], D[:, None], mu[:, None], k,
                                   f[:, None], S0=1.0)
    theta, _, _ = _nlls_init_and_noise(clean, b)
    return theta[:, 1]


def _split(n):
    a, c = N_TRAIN, N_TRAIN + N_CAL
    idx = np.arange(n)
    return idx[:a], idx[a:c], idx[c:]


# --------------------------------------------------------------------------- #
# The dispersion-model (true-Jacobian) wall metric -- companion to
# osipi.wall_metric (bi-exp Jacobian). Stratified on the mu axis.
# --------------------------------------------------------------------------- #
def wall_metric_dispersion(params, k, b, snr):
    """CRLB(mu) under the TRUE dispersion-model Jacobian vs the mu-tercile width."""
    D, mu, f = params[:, 0], params[:, 1], params[:, 2]
    snr = np.broadcast_to(np.asarray(snr, float), (params.shape[0],))
    kk = np.full(params.shape[0], k, float)
    sd = dispersion_crlb_mu_batch(b, D, mu, kk, f, snr, fix_k=True)
    regime, edges = _regime_from_true(mu)
    bin_edges = np.concatenate([[float(mu.min())], edges, [float(mu.max())]])
    bin_w = np.diff(bin_edges)
    finite = np.isfinite(sd)
    med, ratio = [], []
    for r in range(N_REGIME):
        m = (regime == r) & finite
        mm = float(np.median(sd[m])) if m.any() else np.inf
        med.append(mm)
        ratio.append(mm / bin_w[r])
    abs_growth = (med[-1] / med[0]) if med[0] > 0 else np.inf
    return {"med_crlb": med, "ratio": ratio, "hi_ratio": float(ratio[-1]),
            "abs_growth": float(abs_growth)}


# --------------------------------------------------------------------------- #
# Arm 1 -- one dispersion cohort, evaluated CO-PRIMARY under surrogates A & B.
# Reuses osipi._calibration_from_osipi / _evaluate verbatim (swap only the GT).
# --------------------------------------------------------------------------- #
def run_arm1(seed=DEFAULT_SEED, cv=ARM1_CV, alpha=ALPHA, b=DEFAULT_B_VALUES):
    params, snr, sig = _dispersion_pool(seed, cv, b)
    n = params.shape[0]
    tr, ca, te = _split(n)
    k = np.inf if cv <= 0 else 1.0 / (cv * cv)

    # surrogate ground-truth targets (col 1 = D*eff); D and f are exact.
    dstar_B = _bestfit_biexp_dstar(params, cv, b)
    gt = {"A": params.copy(),
          "B": np.column_stack([params[:, 0], dstar_B, params[:, 2]])}

    snr_levels = list(DEFAULT_SNR_GRID)
    out = {"seed": seed, "cv": cv, "n_test": int(te.size)}
    for s in _SURR:
        g = gt[s]
        test_sig, test_gt, test_snr = sig[te], g[te], snr[te]
        # naive transfer: the deployed bi-exp-calibrated predictor (expected break)
        naive_cal = deployed_calibration(b=b, alpha=alpha, seed=seed)
        naive = _evaluate(naive_cal, test_sig, test_gt, b, test_snr, snr_levels, alpha)
        # recalibrated WITHIN the dispersion distribution (the decisive regime)
        recal_cal = _calibration_from_osipi(sig[tr], g[tr], sig[ca], g[ca],
                                            b, alpha, seed)
        recal = _evaluate(recal_cal, test_sig, test_gt, b, test_snr, snr_levels, alpha)
        out[s] = {"naive": naive, "recal": recal}

    # walls: dispersion-Jacobian (true model, surrogate-A mu axis) + bi-exp-fitted
    # (surrogate-B axis); both reported, labeled by Jacobian (CRLB is secondary).
    out["wall_dispJac"] = wall_metric_dispersion(params[te], k, b, snr[te])
    out["wall_biexpFit"] = wall_metric(gt["B"][te], b, snr[te])
    return out


def run_arm1_lognormal(seed=DEFAULT_SEED, cv=ARM1_CV, alpha=ALPHA, b=DEFAULT_B_VALUES):
    """Arm-1 confirmation under a DIFFERENT dispersion kernel (log-normal).

    Same OSIPI 4.8 naive-transfer + within-distribution recalibration path, same
    non-circular surrogate-A axis (D*eff = kernel mean ``mu``), but the ground truth
    is drawn from a log-normal pseudo-diffusivity kernel instead of gamma. If the
    high-D*eff wall persists here too, the circularity verdict is not specific to the
    gamma parametrisation. Reports surrogate A only (the decisive non-circular axis).
    """
    params, snr, sig = _lognormal_pool(seed, cv, b)
    tr, ca, te = _split(params.shape[0])
    gt = params                                         # col 1 = mu = surrogate-A D*eff
    snr_levels = list(DEFAULT_SNR_GRID)
    test_sig, test_gt, test_snr = sig[te], gt[te], snr[te]
    naive_cal = deployed_calibration(b=b, alpha=alpha, seed=seed)
    naive = _evaluate(naive_cal, test_sig, test_gt, b, test_snr, snr_levels, alpha)
    recal_cal = _calibration_from_osipi(sig[tr], gt[tr], sig[ca], gt[ca], b, alpha, seed)
    recal = _evaluate(recal_cal, test_sig, test_gt, b, test_snr, snr_levels, alpha)
    return {"seed": seed, "cv": cv, "n_test": int(te.size),
            "naive": naive, "recal": recal}


# --------------------------------------------------------------------------- #
# Arm 2 -- deployed bi-exp predictor held FIXED; naive transfer across deviation.
# --------------------------------------------------------------------------- #
def _arm2_test(seed, model, dev, b, fam_idx, step_idx):
    rng = np.random.default_rng(seed + 700 + 31 * fam_idx + step_idx)
    params = _draw_params(N_ARM2, rng)
    snr = _draw_snr(N_ARM2, rng, DEFAULT_SNR_GRID)
    if model == "dispersion":
        sig = _simulate(b, params, snr, rng, model="dispersion", disp_cv=dev)
    elif model == "triexp":
        sig = _simulate(b, params, snr, rng, model="triexp", triexp=(4.0, dev))
    elif model == "stretched":
        sig = _simulate(b, params, snr, rng, model="stretched", beta=1.0 - dev)
    else:
        raise ValueError(model)
    return params, snr, sig          # params = nominal (D, D*, f) coverage target


def run_arm2(seed=DEFAULT_SEED, alpha=ALPHA, b=DEFAULT_B_VALUES):
    """Fixed deployed bi-exp calibration, naive-transferred across each family."""
    cal = deployed_calibration(b=b, alpha=alpha, seed=seed)
    fams = {}
    for fi, (model, devs) in enumerate(ARM2_FAMILIES.items()):
        rows = []
        for si, dev in enumerate(devs):
            params, snr, sig = _arm2_test(seed, model, dev, b, fi, si)
            ev = _evaluate(cal, sig, params, b, snr, None, alpha)
            rows.append({"dev": float(dev),
                         "cqr_marg": ev["cqr_marg"], "split_marg": ev["split_marg"],
                         "monitor_auc": ev["monitor_auc"],
                         "monitor_fires": bool(ev["monitor_fires"])})
        fams[model] = rows
    return fams


# --------------------------------------------------------------------------- #
# Flat scalar dict for the multi-seed band harness + consistency gate.
# --------------------------------------------------------------------------- #
def compute_all(seed=DEFAULT_SEED):
    out = {}
    payload = {}
    arm1 = run_arm1(seed=seed)
    payload["arm1"] = arm1
    for s in _SURR:
        for cal in ("naive", "recal"):
            ev = arm1[s][cal]
            for j, nm in enumerate(PARAM_NAMES):
                out[f"altmodel/arm1/{s}/{cal}/cqr_marg/{nm}"] = float(ev["cqr_marg"][j])
            out[f"altmodel/arm1/{s}/{cal}/cqr_hiDstar"] = float(ev["cqr_terc"][-1])
            out[f"altmodel/arm1/{s}/{cal}/cqr_loDstar"] = float(ev["cqr_terc"][0])
        out[f"altmodel/arm1/{s}/naive/monitor_auc"] = float(arm1[s]["naive"]["monitor_auc"])
        out[f"altmodel/arm1/{s}/naive/monitor_fires"] = float(arm1[s]["naive"]["monitor_fires"])
    for tag, wkey in (("dispJac", "wall_dispJac"), ("biexpFit", "wall_biexpFit")):
        w = arm1[wkey]
        for i, kk in enumerate(("lo", "mid", "hi")):
            out[f"altmodel/arm1/wall_{tag}/crlb_over_width/{kk}"] = float(w["ratio"][i])
        out[f"altmodel/arm1/wall_{tag}/abs_growth"] = float(w["abs_growth"])

    # Arm-1 confirmation under a SECOND, independent dispersion kernel (log-normal)
    # on the non-circular surrogate-A axis: does the wall persist under a different
    # kernel shape? (Branch-A robustness to the dispersion parametrisation.)
    arm1_ln = run_arm1_lognormal(seed=seed)
    payload["arm1_lognormal"] = arm1_ln
    for cal in ("naive", "recal"):
        ev = arm1_ln[cal]
        for j, nm in enumerate(PARAM_NAMES):
            out[f"altmodel/arm1_lognormal/A/{cal}/cqr_marg/{nm}"] = float(ev["cqr_marg"][j])
    for kk, idx in (("lo", 0), ("mid", 1), ("hi", 2)):
        out[f"altmodel/arm1_lognormal/A/recal/cqr_{kk}Dstar"] = float(arm1_ln["recal"]["cqr_terc"][idx])
    out["altmodel/arm1_lognormal/A/naive/monitor_auc"] = float(arm1_ln["naive"]["monitor_auc"])

    arm2 = run_arm2(seed=seed)
    payload["arm2"] = arm2
    for model, rows in arm2.items():
        for si, r in enumerate(rows):
            for j, nm in enumerate(PARAM_NAMES):
                out[f"altmodel/arm2/{model}/dev{si}/cov/{nm}"] = float(r["cqr_marg"][j])
            out[f"altmodel/arm2/{model}/dev{si}/auc"] = float(r["monitor_auc"])
            out[f"altmodel/arm2/{model}/dev{si}/fires"] = float(r["monitor_fires"])

    # continuity gate (Checkpoint 3 sanity): cv=0 dispersion == bi-exp control.
    cont = continuity_gate(seed=seed)
    payload["continuity"] = cont
    out["altmodel/continuity/gen_max_abs_err"] = float(cont["gen_max_abs_err"])
    out["altmodel/continuity/cv0_recal_hiDstar_A"] = float(cont["cv0_recal_hiDstar_A"])
    return out, payload


# --------------------------------------------------------------------------- #
# Continuity gate: the dispersion generator at CV=0 must reproduce the
# bi-exponential cohort to numerical tolerance, and Arm 1 at CV=0 must reproduce
# the bi-exp control's recalibrated high-D* number.
# --------------------------------------------------------------------------- #
def continuity_gate(seed=DEFAULT_SEED, b=DEFAULT_B_VALUES):
    from gauge.forward import ivim_signal
    rng = np.random.default_rng(seed + 909)
    params = _draw_params(400, rng)
    D, mu, f = params[:, 0], params[:, 1], params[:, 2]
    clean_disp = ivim_signal_dispersion(b[None, :], D[:, None], mu[:, None],
                                        np.inf, f[:, None], S0=1.0)
    clean_biexp = ivim_signal(b[None, :], D[:, None], mu[:, None], f[:, None], S0=1.0)
    gen_max_abs_err = float(np.max(np.abs(clean_disp - clean_biexp)))
    # Arm 1 at the bi-exp limit (cv=0) -- surrogate A recal high-D* tercile.
    a1_cv0 = run_arm1(seed=seed, cv=0.0)
    cv0_hi_A = float(a1_cv0["A"]["recal"]["cqr_terc"][-1])
    return {"gen_max_abs_err": gen_max_abs_err,
            "cv0_recal_hiDstar_A": cv0_hi_A,
            "cv0_recal_terc_A": [float(x) for x in a1_cv0["A"]["recal"]["cqr_terc"]]}


# --------------------------------------------------------------------------- #
# Verdict + report
# --------------------------------------------------------------------------- #
def _fmt(a):
    return "[" + ", ".join(f"{v:.3f}" for v in a) + "]"


def _verdict(arm1):
    """Branch A (wall general) vs Branch B (bi-exp specific), driven by the
    non-circular surrogate A; surrogate B reported alongside for concordance."""
    recal_hi_A = float(arm1["A"]["recal"]["cqr_terc"][-1])
    recal_hi_B = float(arm1["B"]["recal"]["cqr_terc"][-1])
    gap_A = NOMINAL - recal_hi_A
    gap_B = NOMINAL - recal_hi_B
    persists_A = gap_A >= GAP_TOL
    persists_B = gap_B >= GAP_TOL
    if persists_A:
        branch = "A"
        text = ("BRANCH A -- WALL IS GENERAL / CIRCULARITY BROKEN: the high-D*eff "
                "conditional gap PERSISTS after within-distribution recalibration on "
                "the non-bi-exponential dispersion ground truth "
                f"(surrogate A recal hi-D*eff = {recal_hi_A:.3f}, gap {gap_A:+.3f} vs "
                f"nominal {NOMINAL:.2f}). The wall is a property of perfusion "
                "estimation, not of Eq. (1).")
    else:
        branch = "B"
        text = ("BRANCH B -- WALL IS BI-EXPONENTIAL-SPECIFIC: the high-D*eff gap "
                "VANISHES after recalibration on the dispersion ground truth "
                f"(surrogate A recal hi-D*eff = {recal_hi_A:.3f}, gap {gap_A:+.3f}). "
                "The claim must stay scoped to the bi-exponential model class.")
    return {"branch": branch, "text": text,
            "recal_hi_A": recal_hi_A, "recal_hi_B": recal_hi_B,
            "gap_A": gap_A, "gap_B": gap_B,
            "persists_A": persists_A, "persists_B": persists_B}


