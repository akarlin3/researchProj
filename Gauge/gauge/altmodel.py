    w("ARM 2 -- ENVELOPE: deployed bi-exp calibration, naive transfer across "
      "increasing deviation")
    w("-" * 96)
    for model, rows in arm2.items():
        scalar = {"dispersion": "CV", "triexp": "g", "stretched": "|1-beta|"}[model]
        w(f"  [{model}] deviation scalar = {scalar} (0 = exact bi-exponential):")
        w(f"    {'dev':>6} | {'cov D':>7} | {'cov D*':>7} | {'cov f':>7} | "
          f"{'monitor AUC':>11} | {'fires':>6}")
        cov_fail_dev, fire_dev = None, None
        for r in rows:
            cmin = float(np.min(r["cqr_marg"]))
            failed = cmin < NOMINAL - COV_FAIL_TOL
            if failed and cov_fail_dev is None:
                cov_fail_dev = r["dev"]
            if r["monitor_fires"] and fire_dev is None:
                fire_dev = r["dev"]
            w(f"    {r['dev']:>6.2f} | {r['cqr_marg'][0]:>7.3f} | "
              f"{r['cqr_marg'][1]:>7.3f} | {r['cqr_marg'][2]:>7.3f} | "
              f"{r['monitor_auc']:>11.2f} | "
              f"{('YES' if r['monitor_fires'] else 'no'):>6}")
        rec = rows[0]["cqr_marg"]
        recovered = bool(np.max(np.abs(np.asarray(rec) - NOMINAL)) <= COV_FAIL_TOL)
        before = (fire_dev is not None and
                  (cov_fail_dev is None or fire_dev <= cov_fail_dev))
        w(f"    zero-deviation recovery: dev=0 coverage {_fmt(rec)} "
          f"(within {COV_FAIL_TOL} of nominal: {recovered}).")
        w(f"    monitor first fires at dev={fire_dev}; marginal coverage first "
          f"fails at dev={cov_fail_dev}; fires-before-failure: "
          f"{'YES' if before else 'NO'}.")
        w("")

    w("=" * 96)
    w(f"HEADLINE: Arm-1 Branch {v['branch']} (circularity "
      f"{'broken -- wall general' if v['branch']=='A' else 'wall bi-exp-specific'}); "
      f"continuity gate gen-err {cont['gen_max_abs_err']:.1e}.")
    w(f"SECOND-KERNEL CONFIRMATION (log-normal): Branch-A persists = {ln_persists} "
      f"(recal hi-D*eff {ln_hi:.3f}).")
    w("=" * 96)

    os.makedirs(_RESULTS_DIR, exist_ok=True)
    with open(_REPORT, "w") as fh:
        fh.write("\n".join(lines) + "\n")
    print("\n".join(lines))
    print(f"\n[altmodel] wrote report -> {_REPORT}")
    _write_provenance(seed, payload, v)
    _make_figure(payload, v)
    return 0


