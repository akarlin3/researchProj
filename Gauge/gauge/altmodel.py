"""Alternative-forward-model cohort -- circularity break (Arm 1) + off-model
envelope (Arm 2).

Motivation
----------
The high-D* conditional-coverage wall (Gauge 02/03) was, until now, only ever
demonstrated on cohorts generated by the bi-exponential Eq. (1) -- our synthetic
cohort and the OSIPI DRO. A property of Eq. (1) confirmed only on realizations of
Eq. (1) is circular. There is no in-vivo ground truth, so the only way to break
the circle is ground truth from a DIFFERENT forward model that is NOT
bi-exponential, then ask whether the wall still appears.

Two arms, ONE generator build, kept strictly separate:

* Arm 1 (circularity, 02-3) -- generate ground truth from a genuinely different
  perfusion mechanism (gamma velocity-DISPERSION; ``forward.ivim_signal_dispersion``)
  and RECALIBRATE the conformal predictor WITHIN that distribution (the OSIPI §4.8
  "recalibrated" regime, reused verbatim). Decisive question: does the high-D*eff
  conditional gap PERSIST after within-distribution recalibration? If yes, the wall
  is general (Branch A, circularity broken); if it vanishes, the wall was partly a
  bi-exponential artifact (Branch B). Naive-transfer breaking is expected and is
  NOT the point.

* Arm 2 (envelope, 04-3) -- hold the DEPLOYED bi-exponential-calibrated predictor
  fixed and naive-transfer it across an increasing-deviation family (dispersion CV,
  tri-exp weight, stretched exponent). Report the marginal-coverage degradation
  curve and whether the monitor's AUC tracks the deviation. Coverage breaking is
  the EXPECTED finding; we want the rate and whether the monitor sees it.

D*eff (no literal D* in a dispersion model) is reported CO-PRIMARY under two
surrogates (human sign-off 2026-06-15):
  (A) analytic initial perfusion log-slope = the gamma MEAN ``mu``
      (``forward.dispersion_dstar_eff``) -- model-intrinsic, never references the
      bi-exponential, continuous to the bi-exp limit. The non-circular axis.
  (B) best-fit bi-exponential D* to the (clean) dispersion signal -- the axis a
      bi-exp analyst would see; reported alongside (A) for comparison.

Deterministic from DEFAULT_SEED; reuses gauge.osipi (_calibration_from_osipi /
_evaluate / wall_metric), gauge.invivo.deployed_calibration, and the existing
conformal / monitor / CRLB code unchanged.

Run:  python -m gauge.altmodel              # seed-0 report + figure + provenance
      python -m gauge.altmodel sweep 16     # 16-seed [5,95] bands
"""
